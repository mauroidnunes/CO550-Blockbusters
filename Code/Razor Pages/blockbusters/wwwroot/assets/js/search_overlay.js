function searchOn() {
  document.getElementById("SearchOverlay").style.opacity = "100%";
  document.getElementById("SearchOverlay").style.visibility = "visible";

}

function searchOff() {
  document.getElementById("SearchOverlay").style.visibility = "hidden";
  document.getElementById("SearchOverlay").style.opacity = "0%";
}
function userOn() {
  document.getElementById("UserOverlay").style.display = "block";
}

function userOff() {
  document.getElementById("UserOverlay").style.display = "none";
}
